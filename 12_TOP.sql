-- TOP -> LIMITA LINHAS

SELECT TOP 5
	   CLIENTE,
	   PRODUTO,
	   QUANTIDADE,
	   VENDA_BRUTA
  FROM VENDAS_ANALITICAS

SELECT TOP 5
	   CLIENTE,
	   PRODUTO,
	   QUANTIDADE,
	   VENDA_BRUTA
  FROM VENDAS_ANALITICAS
 ORDER
    BY QUANTIDADE ASC

SELECT TOP 1 PERCENT
	   CLIENTE,
	   PRODUTO,
	   QUANTIDADE,
	   VENDA_BRUTA
  FROM VENDAS_ANALITICAS
 ORDER
    BY QUANTIDADE ASC

